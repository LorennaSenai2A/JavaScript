//incremento e decremneto com valor específico
//operadores de atribuição aritiméticas
var x = 10;
var y = 5;

console.log("-----------------------------");
console.log("x =", x, "| y =", y);

x += y; //x = x + y
console.log("x =", x, "| y=", y);

x -= y; //x = x - y
console.log("x =", x, "| y=", y);

x *= y; //x = x * y
console.log("x =", x, "| y=", y);

x /= y; //x = x / y
console.log("x =", x, "| y=", y);

x **= y; //x = x elevado á y
console.log("x =", x, "| y=", y);

x %= y; //x = resto da divisão inteira de x por y
console.log("x =", x, "| y=", y);

x = 13;
x %= y; //x = resto da divisão inteira de x por y
console.log("x =", x, "| y=", y);



