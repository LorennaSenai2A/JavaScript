//Operadores de incrimento e decremento
var idade = 16;
console.log(idade);

idade++; //idade = idade + 1
console.log(idade);

idade--; //idade = idade - 1
console.log(idade);

console.log("-----------")

var novaIdade = idade++;
console.log("Idade:", idade, "| Nova Idade:", novaIdade);

var novaIdade = ++idade;
console.log("Idade:", idade, "| Nova Idade:", novaIdade);


//incremento e decremento com valor específico
//operadores de atribuições aritiméticas
novaIdade += 10; //novaIdade = novaIdade +10
console.log(novaIdade);

novaIdade -= 20;
console.log(novaIdade); 


